package day03.JavaNumbersAndStrings;

public class JavaNumbers02 {
    public static void main(String[] args) {
        int i = 10;
        System.out.format("%d",i);
        System.out.format("\n");
        System.out.format("%08d",i);
        System.out.format("\n");
        System.out.format("%-d",i);
    }
}
